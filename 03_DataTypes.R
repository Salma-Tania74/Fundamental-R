# Data Types in R
# R has several basic data types including character, numeric, character, and logical.

# 1. Numeric data (discrete, continous)
num1 <- 10
num2 <- 10.5
class(num1)
class(num2)

# 2. Text/Character data
name <- "Rahim"
class(name)

# 3. Logical data (T/F)
is_weekday <- FALSE
class(is_weekday)

